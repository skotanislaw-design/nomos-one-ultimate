def route_question(question: str) -> str:
    """
    Returns the specialist id best matching the question.
    Uses simple keyword count. Ties → 'civil' (most common).
    No Claude API call — instant and free.
    """
    q_lower = question.lower()
    scores: dict[str, int] = {}

    for sid, spec in SPECIALISTS.items():
        count = 0
        for kw in spec["keywords"]:
            if kw.lower() in q_lower:
                count += 1
        scores[sid] = count

    best_id = max(scores, key=lambda k: scores[k])
    if scores[best_id] == 0:
        return "civil"  # default: most common domain
    return best_id


